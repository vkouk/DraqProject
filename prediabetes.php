        <p>
            <strong>Prediabetes</strong> is the condition that is characterized by blood glucose levels being higher than normal, but at the same time not so high as to be diagnosed as any type of diabetes. When having prediabetes, the glucose levels are disrupted during the morning or 2 hours after each meal.
        </p>
        <p>
            <strong>Having prediabetes doesn’t necessarily mean that the patient will also develop diabetes, but that there is increased risk to do so.</strong>
            Additionally having prediabetes is a serious risk factor for heart disease. Similar to patients who have type 2 diabetes, prediabetic people tend to be overweight, have high blood pressure, and elevated cholesterol levels.
        </p>
        <h4>
            Symptoms:
        </h4>
        <p>
            Prediabetes is often a “silent” condition because it usually shows no specific symptoms. Someone could have prediabetes for years without knowing it. Although, certain risk factors increase the chance of having prediabetes. Those are:
        <ul>
            <li>Being overweight</li>
            <li>Being 45 years old and older</li>
            <li>Having a family history of diabetes</li>
            <li>Having low level of high density lipoproteins (HDL cholesterol)</li>
            <li>Having high levels of triglycerides</li>
            <li>Having high blood pressure</li>
            <li>Have had at some point gestational diabetes</li>
        </ul>
        </p>
        Population groups with increased risk of developing diabetes that should be tested for prediabetes are the following:
        <p>
        <ul>
            <li>People with family history of Diabetes</li>
            <li>People with cardiovascular disease</li>
            <li>Obese or living a sedentary lifestyle people</li>
            <li>People with hypertension or hyperlipidemia</li>
            <li>People on antipsycotic medication</li>
            <li>Women with history of gestational diabetes or polycystic ovary syndrome</li>
        </ul>
        </p>
        From the moment any of the above are found, more often checkups should occur (at least once a year or every 6 months). Some of these conditions are not permanent. Usually they can (and should) go away with proper diet, weight loss, and a systematic exercise program, in order for the condition to not develop into diabetes.
