        <h3>What is Diabetes</h3>
        <p>Diabetes is a metabolic disease, causing the blood to have an abnormal concentration of sugar and disturbing its metabolism of glucose, either as a result of reduced insulin secretion or due to lower body cell sensitivity to insulin. Patients suffering from Diabetes have significantly increased levels of glucose in the blood. This is due to the pancreas not producing enough insulin or the body cells not responding properly to the insulin produced by it. The result is an increased circulation of glucose in the blood is perceived. The body eliminates the aforementioned excess glucose through urination. Thus, if the blood has sufficient glucose levels, the cells cannot absorb it to satisfy the body’s energy needs.</p>
        <h4>Types of Diabetes:</h4>
        <p><strong>Diabetes Type 1</strong> – No insulin is produced.<br>
            <strong>Diabetes Type 2</strong>– Insufficient insulin is produced or the produced insulin does not serve its purpose.<br>
            <strong>Gestational Diabetes</strong> – The onset of Diabetes during pregnancy. This type resembles Diabetes Type 2 with regards to concurrent reduced insuline secretion and reduced cell sensitivity to insulin. Obese women are more likely to develop Gestational Diabetes. This type of Diabetes occurs in 3-5% of pregnancies. 30-40% of women with Gestational Diabetes will develop Diabetes Type 2 later in their life. Gestational Diabetes is reversible and subsides after delivery, it may though cause perinatal complications and problems to the mother’s and baby’s health.</p>
        <h4>Other types of Diabetes</h4>
        <span>Diabetes can also occur in the following cases:</span>
        <ul>
            <li>Genetic defects of Pancreas Beta cells.</li>
            <li>Genetic disorder of Insulin action.</li>
            <li>Pancreas diseases.</li>
            <li>Hormonal disorders and endocrinopathies.</li>
            <li>Medicine induced Diabetes.</li>
        </ul>
        <h4>Epidemiological Data</h4>
        <p>According to the <strong>World Health Organization</strong> in 2006 Diabetes patients exceed 170 million worldwide, a number that is expected to double until 2030 as the disease incidence is rapidly increasing. It is estimated that in Greece 5.9% of the general population suffers from Diabetes. Diabetes is more frequent in the developed world (especially Type 2) and it appears to be directly correlated with the so called western way of life, i.e. living in large urban areas, unhealthy dietary habits and sedentary lifestyle. It is one of the leading causes of death worldwide.</p>
        <h4>Symptoms:</h4>
        <span>The typical symptoms of Diabetes include:</span>
        <ul>
            <li>Increased urine production.</li>
            <li>Increased thirst.</li>
            <li>Increased appetite.</li>
            <li>Body weight loss.</li>
        </ul>
        <span><strong>Diabetes Type 1</strong> onset is usually abrupt and the first symptoms may be:</span>
        <ul>
            <li>Development of diabetic ketoacidosis.</li>
            <li>Nausea.</li>
            <li>Vomiting.</li>
            <li>Diffused abdominal pain.</li>
            <li>Loss of conciousness.</li>
        </ul>
        <p>
            <strong>Diabetes Type 2</strong> onset is gradual, overeating is less common, whereas fatigue, nausea and decreased immune response to infections frequently coexist. In many cases Diabetes Type 2 symptoms are totally absent and the patient feels healthy.
        </p>
        <h4>Important Conditions related to Diabetes</h4>
        <span><strong>1.Hypoglycemia</strong></span>
        <p>
            Hypoglycemia is also referred to as insulin resistance. It occurs when the blood’s glucose levels are low.
        </p>
        <span><strong>Symptoms of Hypoglycemia</strong></span>
        <ul>
            <li>Tingling sensation around the mouth</li>
            <li>Dizziness.</li>
            <li>Sweating</li>
            <li>Shaking</li>
            <li>Headache</li>
            <li>Pale skin</li>
            <li>Irritability</li>
            <li>Eye tearing</li>
            <li>Spasms</li>
            <li>Attention Deficit Disorder</li>
            <li>Impaired judgement</li>
            <li>Convulsions</li>
            <li>Feeling of hunger</li>
        </ul>
        <span><strong>Treating Hypoglycemia</strong></span>
        <p>
            In order to treat Hypoglycemia the blood’s glucose levels need to go up. Quickest way is to eat food or drink liquids that contain sugar.
        <ul>
            <li>A teaspoon of sugar</li>
            <li>1/2 cup of juice</li>
            <li>5-6 candies</li>
        </ul>
        </p>
        <span><strong>2.Hyperglycemia</strong></span>
        <p>
            <strong>Hyperglycemia is the condition that occurs when the blood’s glucose levels are extremely high.</strong>It occurs when there is not any or enough insulin in the blood or if the insulin that the body produces has no effect. When it comes to diabetes patients one can develop hyperglycemia if they miss of forget a dose of their diabetic medication.
        </p>
        <span><strong>Symptoms of Hyperglycemia</strong></span>
        <ul>
            <li>High blood sugar</li>
            <li>High glucose levels in urine</li>
            <li>Increased hunger</li>
            <li>Increased thirst</li>
            <li>Increased urination</li>
            <li>Blurred vision</li>
            <li>Weight loss</li>
            <li>Poor wound healing</li>
            <li>Dry mouth</li>
            <li>Cardiac arrhythmia</li>
            <li>Deep and rapid breathing</li>
            <li>Erectile dysfunction</li>
            <li>Itchy and dry skin</li>
            <li>Fatigue</li>
            <li>Seizures</li>
            <li>Coma</li>
        </ul>
        <span><strong>Treating Hyperglycemia</strong></span>
        <p>
            If someone is dealing with Hyperglycemia’s symptoms they should check their blood sugar levels and in case they are high, reach out and contact their doctor.
        </p>
        <h4>Diagnosis</h4>
        <p>
            The diagnostic criteria that have been established for the diagnosis of diabetes are:
        <ul>
            <li>The presence of the classic diabetes symptoms and casual plasma glucose >200 mg/dl</li>
            <li>Fasting plasma glucose levels >126 mg/dl</li>
            <li>Plasma glucose 2 hours after a 75g glucose oral load >200 mg/dl</li>
            <li>Another way is measuring the glycated hemoglobin levels (HbA1c). It is commonly used to keep track of diabetes rather than diagnosing it, since it measures a three month average plasma glucose concentration</li>
        </ul>
        </p>
        <h4>
            Treating Diabetes
        </h4>
        <p>
            A very important part in the treatment of diabetes is the proper education of the diabetic patient regarding the nutrition that needs to be followed, the checkup of their blood sugar by themselves, and the insulin administration. The coping strategy is focusing on the long-term glycemic control of the patient and also the elimination of factors that increase the risk of chronic complications of diabetes, like quitting smoking, controlling blood pressure and cholesterol levels, the increase of physical exercise, and the overall adoption of a healthier lifestyle. In cases of type 1 diabetes, the insulin administration is required to ensure the desired glucose levels. On the other hand, in cases of type 2 diabetes, glycemic control can be achieved initially by weight control and diet, or oral administration of antidiabetic drugs. If those fail, insulin will need to be administered. The most common method of insulin administration is the subcutaneous administration. The insulin type, dosage, and the frequency of administration depend upon the needs of each patient. That is why every insulin therapy plan is completely personalized for each case.
        </p>


