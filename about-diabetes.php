    <h3 align="center">What is Diabetes</h3>
    <p>Diabetes is a metabolic disease, causing the blood to have an abnormal concentration of sugar and disturbing its metabolism of glucose, either as a result of reduced insulin secretion or due to lower body cell sensitivity to insulin.
        Patients suffering from Diabetes have significantly increased levels of glucose in the blood.  This is due to the pancreas not producing enough insulin or the body cells not responding properly to the insulin produced by it. The result is an increased circulation of glucose in the blood is perceived. The body eliminates the aforementioned excess glucose through urination. Thus, if the blood has sufficient glucose levels, the cells cannot absorb it to satisfy the body’s energy needs.</p>
    <h4>Types of Diabetes:</h4>
    <p>Diabetes Type 1 – No insulin is produced.<br>
        Diabetes Type 2 – Insufficient insulin is produced or the produced insulin does not serve its purpose.<br>
        Gestational Diabetes – The onset of Diabetes during pregnancy. This type resembles Diabetes Type 2 with regards to concurrent reduced insuline secretion and reduced cell sensitivity to insulin. Obese women are more likely to develop Gestational Diabetes. This type of Diabetes occurs in 3-5% of pregnancies. 30-40% of women with Gestational Diabetes will develop Diabetes Type 2 later in their life. Gestational Diabetes is reversible and subsides after delivery, it may though cause perinatal complications and problems to the mother’s and baby’s health.
    </p>
    <h4>Other types of Diabetes</h4>
    <p>Diabetes can also occur in the following cases:<br>
        <ul>
        <li>Genetic defects of Pancreas Beta cells.</li>
        <li>Genetic disorder of Insulin action./li>
        <li>Pancreas diseases.</li>
        <li>Hormonal disorders and endocrinopathies.</li>
        <li>Medicine induced Diabetes.</li>
        </ul>
    </p>
    <h4>Epidemiological Data</h4>
    <p>According to the World Health Organization in 2006 Diabetes patients exceed 170 million worldwide, a number that is expected to double until 2030 as the disease incidence is rapidly increasing. It is estimated that in Greece 5.9% of the general population suffers from Diabetes. Diabetes is more frequent in the developed world (especially Type 2) and it appears to be directly correlated with the so called western way of life, i.e. living in large urban areas, unhealthy dietary habits and sedentary lifestyle. It is one of the leading causes of death worldwide.


    </p>
    <h4>Symptoms:</h4>
    <p>The typical symptoms of Diabetes include:<br>
        <ul>
        <li>Increased urine production.</li>
        <li>Increased thirst.</li>
        <li>Increased appetite.</li>
        <li>Body weight loss.</li>
        </ul>

        Diabetes Type 1 onset is usually abrupt and the first symptoms may be:<br>
        <ul>
            <li>Development of diabetic ketoacidosis.</li>
            <li>Nausea.</li>
            <li>Vomiting.</li>
            <li>Diffused abdominal pain.</li>
            <li>Loss of conciousness.</li>

        </ul>
        Diabetes Type 2 onset is gradual, overeating is less common, whereas fatigue, nausea and decreased immune response to infections frequently coexist. In many cases Diabetes Type 2 symptoms are totally absent and the patient feels healthy.
    </p>

	
